update content_factory.training_modules module
set
  content = jsonb_set(
    coalesce(module.content, '{}'::jsonb),
    '{interactive_walkthroughs}',
    catalog.walkthroughs,
    true
  ),
  updated_at = now()
from walkthrough_catalog catalog
where module.code = catalog.module_code
  and module.module_type = 'course';

do $training_interactive_contract$
declare
  matching_courses integer;
  walkthrough_count integer;
  malformed_walkthroughs integer;
begin
  select
    count(*),
    coalesce(sum(jsonb_array_length(module.content -> 'interactive_walkthroughs')), 0)
  into matching_courses, walkthrough_count
  from content_factory.training_modules module
  where module.code = any(array[
    'factory_basics',
    'video_quality',
    'publishing_funnel',
    'security_wb'
  ])
    and module.module_type = 'course'
    and jsonb_typeof(module.content -> 'interactive_walkthroughs') = 'array';

  if matching_courses <> 4 or walkthrough_count <> 8 then
    raise exception 'training interactive catalog is incomplete';
  end if;

  select count(*)
  into malformed_walkthroughs
  from content_factory.training_modules module
  cross join lateral jsonb_array_elements(
    module.content -> 'interactive_walkthroughs'
  ) walkthrough
  where module.code = any(array[
    'factory_basics',
    'video_quality',
    'publishing_funnel',
    'security_wb'
  ])
    and (
      coalesce(walkthrough ->> 'id', '') !~ '^[a-z0-9_]{3,80}$'
      or jsonb_typeof(walkthrough -> 'frames') <> 'array'
      or jsonb_array_length(walkthrough -> 'frames') < 2
      or jsonb_typeof(walkthrough -> 'transcript') <> 'array'
      or jsonb_array_length(walkthrough -> 'transcript') < 2
      or jsonb_typeof(walkthrough -> 'checklist') <> 'array'
      or jsonb_array_length(walkthrough -> 'checklist') < 2
    );

  if malformed_walkthroughs <> 0 then
    raise exception 'training interactive catalog contains malformed walkthroughs';
  end if;
end;
$training_interactive_contract$;

commit;
