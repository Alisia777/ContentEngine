            "Статус «Одобрено» не принят за факт перевода",
            "Завершение подтверждено статусом «Выплачено»"
          ],
          "transcript": [
            {"time": "До начала", "text": "Сначала проверьте зафиксированную сумму и условия задачи."},
            {"time": "После проверки", "text": "Одобрение результата ещё не означает, что внешний перевод выполнен."},
