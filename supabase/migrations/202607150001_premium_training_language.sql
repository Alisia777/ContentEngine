do $training_visual_contract$
declare
  expected_course_codes constant text[] := array[
    'factory_basics',
    'publishing_funnel',
    'security_wb',
    'video_quality'
  ]::text[];
  active_course_codes text[];
  invalid_content_count integer;
  active_exam_count integer;
  active_exam_code text;
  active_exam_pass_score integer;
  active_exam_declared_questions integer;
  active_exam_actual_questions integer;
  public_course_question_count integer;
  server_course_question_count integer;
  private_course_key_count integer;
  leaked_course_answer_count integer;
begin
  select coalesce(array_agg(module.code order by module.code), array[]::text[])
    into active_course_codes
  from content_factory.training_modules module
  where module.module_type = 'course'
    and module.is_active;

  select count(*)
    into invalid_content_count
  from content_factory.training_modules module
  where module.code = any(expected_course_codes)
    and (
      module.module_type <> 'course'
      or not module.is_active
      or module.content ->> 'version' is distinct from '2'
      or not (module.content ? 'block_label')
      or not (module.content ? 'duration_minutes')
      or not (module.content ? 'outcome')
      or jsonb_typeof(module.content -> 'completion_checklist') is distinct from 'array'
      or jsonb_typeof(module.content -> 'knowledge_check') is distinct from 'object'
      or module.content #>> '{knowledge_check,pass_score}' is distinct from case
        when module.code = 'publishing_funnel' then '5'
        else '3'
      end
      or case
        when jsonb_typeof(module.content #> '{knowledge_check,questions}') = 'array'
          then jsonb_array_length(module.content #> '{knowledge_check,questions}')
        else -1
      end <> case
        when module.code = 'publishing_funnel' then 5
        else 3
      end
      or case
        when jsonb_typeof(module.content -> 'lessons') = 'array'
          then jsonb_array_length(module.content -> 'lessons')
        else -1
      end < case
        when module.code = 'publishing_funnel' then 12
        else 6
      end
    );

  select
    count(*),
    max(module.code),
    max(module.pass_score),
    max(module.question_count)
  into
    active_exam_count,
    active_exam_code,
    active_exam_pass_score,
    active_exam_declared_questions
  from content_factory.training_modules module
  where module.module_type = 'exam'
    and module.is_active;

  select count(*)
    into active_exam_actual_questions
  from content_factory.training_questions question
  where question.module_code = 'operator_final_exam';

  select coalesce(sum(jsonb_array_length(
    module.content #> '{knowledge_check,questions}'
  )), 0)
    into public_course_question_count
  from content_factory.training_modules module
  where module.module_type = 'course'
    and module.is_active;

  select count(*)
    into server_course_question_count
  from content_factory.training_questions question
  join content_factory.training_modules module
    on module.code = question.module_code
   and module.module_type = 'course'
   and module.is_active
  where question.order_index between 901 and 1000
    and strpos(question.code, 'course_check_' || module.code || '_') = 1;

  select count(*)
    into private_course_key_count
  from content_factory_private.training_answer_keys answer_key
  join content_factory.training_questions question
    on question.code = answer_key.question_code
  join content_factory.training_modules module
    on module.code = question.module_code
   and module.module_type = 'course'
   and module.is_active
  where question.order_index between 901 and 1000
    and strpos(question.code, 'course_check_' || module.code || '_') = 1;

  select count(*)
    into leaked_course_answer_count
  from content_factory.training_modules module
  where module.module_type = 'course'
    and (
      jsonb_path_exists(
        module.content,
        '$.knowledge_check.questions[*].correct_value'
      )
      or jsonb_path_exists(
        module.content,
        '$.knowledge_check.questions[*].explanation'
      )
    );

  if active_course_codes is distinct from expected_course_codes
     or invalid_content_count <> 0
     or active_exam_count <> 1
     or active_exam_code <> 'operator_final_exam'
     or active_exam_pass_score <> 10
     or active_exam_declared_questions <> 12
     or active_exam_actual_questions <> 12
     or public_course_question_count < 1
     or server_course_question_count <> public_course_question_count
     or private_course_key_count <> public_course_question_count
     or leaked_course_answer_count <> 0 then
    raise exception using
      errcode = '23514',
      message = 'premium_training_language_contract_failed',
      detail = format(
        'courses=%s invalid_content=%s exam_count=%s exam_code=%s pass_score=%s declared_questions=%s actual_questions=%s public_course_questions=%s server_course_questions=%s private_course_keys=%s leaked_course_answers=%s',
        active_course_codes,
        invalid_content_count,
        active_exam_count,
        active_exam_code,
        active_exam_pass_score,
        active_exam_declared_questions,
        active_exam_actual_questions,
        public_course_question_count,
        server_course_question_count,
        private_course_key_count,
        leaked_course_answer_count
      );
  end if;
end;
