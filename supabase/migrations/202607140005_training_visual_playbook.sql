do $training_visual_contract$
declare
  expected_course_codes constant text[] := array[
    'factory_basics',
    'publishing_funnel',
    'security_wb',
    'video_quality'
  ]::text[];
  active_course_codes text[];
  invalid_content_count integer;
  active_exam_count integer;
  active_exam_code text;
  active_exam_pass_score integer;
  active_exam_declared_questions integer;
  active_exam_actual_questions integer;
begin
  select coalesce(array_agg(module.code order by module.code), array[]::text[])
    into active_course_codes
  from content_factory.training_modules module
  where module.module_type = 'course'
    and module.is_active;

  select count(*)
    into invalid_content_count
  from content_factory.training_modules module
  where module.code = any(expected_course_codes)
    and (
      module.module_type <> 'course'
      or not module.is_active
      or module.content ->> 'version' is distinct from '2'
      or not (module.content ? 'duration_minutes')
      or not (module.content ? 'outcome')
      or jsonb_typeof(module.content -> 'completion_checklist') is distinct from 'array'
      or case
        when jsonb_typeof(module.content -> 'lessons') = 'array'
          then jsonb_array_length(module.content -> 'lessons')
        else -1
      end < case
        when module.code = 'publishing_funnel' then 7
        else 5
      end
    );

  select
    count(*),
    max(module.code),
    max(module.pass_score),
    max(module.question_count)
  into
    active_exam_count,
    active_exam_code,
    active_exam_pass_score,
    active_exam_declared_questions
  from content_factory.training_modules module
  where module.module_type = 'exam'
    and module.is_active;

  select count(*)
    into active_exam_actual_questions
  from content_factory.training_questions question
  where question.module_code = 'operator_final_exam';

  if active_course_codes is distinct from expected_course_codes
     or invalid_content_count <> 0
     or active_exam_count <> 1
     or active_exam_code <> 'operator_final_exam'
     or active_exam_pass_score <> 10
     or active_exam_declared_questions <> 12
     or active_exam_actual_questions <> 12 then
    raise exception using
      errcode = '23514',
      message = 'training_visual_catalog_contract_failed',
      detail = format(
        'courses=%s invalid_content=%s exam_count=%s exam_code=%s pass_score=%s declared_questions=%s actual_questions=%s',
        active_course_codes,
        invalid_content_count,
        active_exam_count,
        active_exam_code,
        active_exam_pass_score,
        active_exam_declared_questions,
        active_exam_actual_questions
      );
  end if;
end;
